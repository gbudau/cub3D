#include "libft.h"

void	ft_lstrev(t_list **lst)
{
	t_list *prev;
	t_list *curr;
	t_list *next;

	prev = NULL;
	curr = *lst;
	next = NULL;
	while (curr != NULL)
	{
		next = curr->next;
		curr->next = prev;
		prev = curr;
		curr = next;
	}
	*lst = prev;
}
